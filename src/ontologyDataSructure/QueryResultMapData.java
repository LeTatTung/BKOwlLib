package ontologyDataSructure;

import java.util.*;



public class QueryResultMapData {
	/**
	 * Danh sách các biến
	 */
	private ArrayList<String> VariableList;
	/**
	 * Map các cặp biến và giá trị
	 */
	private Map<String,ArrayList<String>> ResultMap;
	
	//Các hàm getters và setters
	public ArrayList<String> getVariableList() {
		return VariableList;
	}
	public void setVariableList(ArrayList<String> variableList) {
		VariableList = variableList;
	}
	public Map<String,ArrayList<String>> getResultMap() {
		return ResultMap;
	}
	public void setResultMap(Map<String,ArrayList<String>> resultMap) {
		ResultMap = resultMap;
	}
	
	//Default Constructor
	public QueryResultMapData(){
		
	}
	//Constructor
	public QueryResultMapData(ArrayList<String> variableList,Map<String,ArrayList<String>> resultMap) {
		super();
		VariableList = variableList;
		ResultMap = resultMap;
	}
	

	
}
